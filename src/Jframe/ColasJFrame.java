    private void jButtonEncolarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEncolarActionPerformed
         try{ //al momento de encolar si esta vacia se manda una mensaje que introdusca el cliente
             String cliente=jTextCliente.getText();
             cola.encolar(cliente);//ejecuta el metodo encola
             String s = (String)
             cola.mostrar();//ejecuta el metodo mostrar
             jTextAreaResultado.setText(s);
              jTextCliente.setText("");
             jTextCliente.requestFocus();
         }catch (Exception ex){
             JOptionPane.showMessageDialog(null, "Ingrese el nombre del cliente");
         }
        
        
    }//GEN-LAST:event_jButtonEncolarActionPerformed

