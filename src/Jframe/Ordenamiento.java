    public int[] ordenar(int arreglo[])
    {      
       
       int min,aux,temp;
        for(int i=0;i<arreglo.length-1;i++){ 
            min= arreglo[i];                        
            aux =i;
           for(int o=i+1;o<arreglo.length;o++){
                
                if(arreglo[o]<min){
                    min = arreglo[o];
                    aux= o;
                }
          }
            if(aux !=i){
                temp = arreglo[i];
                arreglo[i]=arreglo[aux];
                arreglo[aux]=temp;
            }
        }
        return arreglo;
    }

